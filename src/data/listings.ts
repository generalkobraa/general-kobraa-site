export function getListingsByCategory(categorySlug: string): Listing[] {
  return listings.filter((l) => l.category === categorySlug);
}

export function getFeaturedListings(): Listing[] {
  return listings.filter((l) => l.featured);
}

export function getSponsoredListings(): Listing[] {
  return listings.filter((l) => l.sponsored);
}

export function getListingBySlug(slug: string): Listing | undefined {
  return listings.find((l) => l.slug === slug);
}

export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find((c) => c.slug === slug);
}

export function searchListings(query: string): Listing[] {
  const q = query.toLowerCase();
  return listings.filter(
    (l) =>
      l.name.toLowerCase().includes(q) ||
      l.tagline.toLowerCase().includes(q) ||
      l.description.toLowerCase().includes(q) ||
      l.tags.some((t) => t.toLowerCase().includes(q)) ||
      l.subcategory.toLowerCase().includes(q)
  );
}
